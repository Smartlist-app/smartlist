      function disable() {
        document.getElementById('__btn').disabled = true;
        document.getElementById('gHusername').style.display = 'none';
        document.getElementById('__btn').innerHTML = 'Sorry, you need to use GitHub to join Smartlist';
        __selected = 1;
      }
      function enable() {
        document.getElementById('__btn').disabled = false;
        document.getElementById('gHusername').style.display = 'block';
        document.getElementById('gHusername').getElementsByTagName('input')[0].focus()
        document.getElementById('__btn').innerHTML = 'Submit';
        __selected = 2;
      }
      function __open() {
          $('#modal1').modal('open');
          localStorage.setItem('s', 'true')
      }
      var prevScrollpos = window.pageYOffset;
      window.onscroll = function() {
        var currentScrollPos = window.pageYOffset;
        if (prevScrollpos > currentScrollPos) {
          document.getElementById("navbar").style.top = "0";
        } else {
          document.getElementById("navbar").style.top = "-" + window.pageYOffset+ "px";
        }
        prevScrollpos = currentScrollPos;
      }
      var form = document.getElementById("my-form");
      async function handleSubmit(event) {
        event.preventDefault();
        var status = document.getElementById("my-form-status");
        var data = new FormData(event.target);
        fetch(event.target.action, {
          method: form.method,
          body: data,
          headers: {
            'Accept': 'application/json'
          }
        }).then(response => {
          __open();
          form.reset();
        }).catch(error => {
          status.innerHTML = "Oops! There was a problem submitting your form";
          document.getElementById('__btn').innerHTML = "Oops! There was a problem submitting your form! Please reload the page";
          document.getElementById('__btn').disabled = true;
        });
      }
      form.addEventListener("submit", handleSubmit);
